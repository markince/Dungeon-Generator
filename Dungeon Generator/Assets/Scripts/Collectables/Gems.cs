public class Gems : MonoBehaviour
{
    //***************************************************************************************************************************************//
    //	Public Variables                                                                                                                     //
    //***************************************************************************************************************************************//

    public Text gemAmountText;


    //***************************************************************************************************************************************//
    //	Private Variables                                                                                                                    //
    //***************************************************************************************************************************************//

    [SerializeField] int gemAmount = 0;

    //***************************************************************************************************************************************//
    //	Update Function                                                                                                                      //
    //***************************************************************************************************************************************//

    private void Update()
    {
        gemAmountText.text = gemAmount.ToString();
    }

    //***************************************************************************************************************************************//
    //	Class functions                                                                                                                      //
    //***************************************************************************************************************************************//

    //---------------------------------------------------------------------------------------------------------------------------------------//
    // GetCurrentGems                                                                                                                        //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    // Function to get the number of gems the player has                                                                                     //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//

    public int GetCurrentGems()
    {
        return gemAmount;
    }

    //---------------------------------------------------------------------------------------------------------------------------------------//
    // IncreaseCurrentGems                                                                                                                   //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    // Function that increase the number of gems the player has. This is called when the player collects a gem.                              //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//


    public void IncreaseCurrentGems(int _gemAmount)
    {
        gemAmount += _gemAmount;
    }
}
