//  Notes:  Modifies the player ammo when they collect an ammo item                                                                          //
//                                                                                                                                           //
//*******************************************************************************************************************************************//

public class AmmoCollection : MonoBehaviour
{
    //***************************************************************************************************************************************//
    //	Public Variables                                                                                                                     //
    //***************************************************************************************************************************************//

    public AudioSource audioSource;
    public AudioClip collectBookSFX;

    //***************************************************************************************************************************************//
    //	Private Variables                                                                                                                    //
    //***************************************************************************************************************************************//

    [SerializeField] int ammoAmount = 5;
    bool collected = false;

    //***************************************************************************************************************************************//
    //	Class functions                                                                                                                      //
    //***************************************************************************************************************************************//
    //---------------------------------------------------------------------------------------------------------------------------------------//
    // OnTriggerEnter                                                                                                                        //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    // Function to check for collision with the player. The ammo of the player is then increased.                                            //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//

    private void OnTriggerEnter(Collider other)
    {
        if (other.gameObject.tag == "Player")
        {
            if (!collected)
            {
                FindObjectOfType<Ammo>().IncreaseCurrentAmmo(ammoAmount);
                collected = true;
            }


            // Play Sound effect
            audioSource.PlayOneShot(collectBookSFX);

            Destroy(gameObject, 0.5f);
        }

    }
}
