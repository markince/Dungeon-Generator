using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.SceneManagement;

//*******************************************************************************************************************************************//
//  File:   SceneLoader.cs                                                                                                                   //
//                                                                                                                                           //
//  Author: Mark Ince                                                                                                                        //
//                                                                                                                                           //
//  Date:   03/11/2020                                                                                                                       //
//                                                                                                                                           //
//  Info:   Scene loader for BSP scene                                                                                                       //
//                                                                                                                                           //
//  Notes:  Loads the relevent scene when the F1 or F2 buttons are pressed while in game mode                                                //
//                                                                                                                                           //
//*******************************************************************************************************************************************//

public class SceneLoader : MonoBehaviour
{
    //***************************************************************************************************************************************//
    //	Update Function                                                                                                                      //
    //***************************************************************************************************************************************//

    private void Update()
    {
        if (Input.GetKeyDown(KeyCode.F1))
        {
            // Load BSP scene
            SceneManager.LoadScene(1);

            Cursor.lockState = CursorLockMode.None;
            Cursor.visible = true;

        }

        if (Input.GetKeyDown(KeyCode.F2))
        {
            // Load Front title scene
            SceneManager.LoadScene(0);

            Cursor.lockState = CursorLockMode.None;
            Cursor.visible = true;

        }

    }

    //***************************************************************************************************************************************//
    //	Class functions                                                                                                                      //
    //***************************************************************************************************************************************//

    //---------------------------------------------------------------------------------------------------------------------------------------//
    //  ReloadGame                                                                                                                           //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    //  Function to reload the BSP scene                                                                                                     //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//

    public void ReloadGame()
    {
        SceneManager.LoadScene(1);
        Time.timeScale = 1;
    }

    //---------------------------------------------------------------------------------------------------------------------------------------//
    //  ReturnToMainMenu                                                                                                                     //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    //  Function that returns the user to the front scene                                                                                    //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//


    public void ReturnToMainMenu()
    {
        SceneManager.LoadScene(0);
        Time.timeScale = 1;
    }

    //---------------------------------------------------------------------------------------------------------------------------------------//
    //  QuitGame                                                                                                                             //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    //  Function that is called when the player quits the X button                                                                           //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//

    public void QuitGame()
    {
        Application.Quit();
    }

}
