public class DeathHandler : MonoBehaviour
{
    //***************************************************************************************************************************************//
    //	Public  Variables                                                                                                                    //
    //***************************************************************************************************************************************//

    [SerializeField] Canvas gameOverCanvas = null;

    //***************************************************************************************************************************************//
    //	Class functions                                                                                                                      //
    //***************************************************************************************************************************************//

    //---------------------------------------------------------------------------------------------------------------------------------------//
    //  HandleDeath                                                                                                                          //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    // Function called when the players health is reduced to zero                                                                            //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//

    public void HandleDeath()
    {
        // Enable game over canvas
        gameOverCanvas.enabled = true;
        
        // Stop game time
        Time.timeScale = 0;
        
        // Activate the cursor so player can click buttons
        Cursor.lockState = CursorLockMode.None;
        Cursor.visible = true;
    }

} // End of DeathHandler class
