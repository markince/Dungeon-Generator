public class BSPUIPlayButton : MonoBehaviour
{
    //***************************************************************************************************************************************//
    //	Public Variables                                                                                                                     //
    //***************************************************************************************************************************************//


    public Camera dungeonCreatorCamera;
    public GameObject player;
    public Canvas dungeonGeneratorUI;
    public Canvas mainGameCanvas;
    public AudioListener listener;

    //***************************************************************************************************************************************//
    //	Start Function                                                                                                                       //
    //***************************************************************************************************************************************//

    private void Start()
    {
        listener = GetComponent<AudioListener>();
    }

    //***************************************************************************************************************************************//
    //	Class functions                                                                                                                      //
    //***************************************************************************************************************************************//

    //---------------------------------------------------------------------------------------------------------------------------------------//
    // StartGame                                                                                                                             //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    // Function called when the play button is clicked                                                                                       //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//


    public void StartGame()
    {
        dungeonCreatorCamera.enabled = false;
        listener.enabled = false;
        player.SetActive(true);
        dungeonGeneratorUI.enabled = false;
        mainGameCanvas.enabled = true;
    }

}
