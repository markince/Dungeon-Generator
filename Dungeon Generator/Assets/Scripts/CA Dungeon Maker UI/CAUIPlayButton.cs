public class CAUIPlayButton : MonoBehaviour
{
    //***************************************************************************************************************************************//
    //	Public Variables                                                                                                                     //
    //***************************************************************************************************************************************//


    public Camera dungeonCreatorCamera;
    public GameObject player;
    public Canvas dungeonGeneratorUI;
    public Canvas inGameUI;

    //***************************************************************************************************************************************//
    //	Class functions                                                                                                                      //
    //***************************************************************************************************************************************//

    //---------------------------------------------------------------------------------------------------------------------------------------//
    // StartGame                                                                                                                             //
    // --------------------------------------------------------------------------------------------------------------------------------------//
    // Fntion to start the game when the play button is pressed                                                                              //
    //                                                                                                                                       //
    // --------------------------------------------------------------------------------------------------------------------------------------//


    public void StartGame()
    {
        dungeonCreatorCamera.enabled = false;
        dungeonGeneratorUI.enabled = false;
        player.SetActive(true);

        inGameUI.enabled = true;

    }
}
